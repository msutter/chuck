require 'awesome_print'

map 'http://bmproxy', 'http://localhost:8443'

on_request do |request|
	puts "#################################################################################"
	puts "---------------------------------------------------------------------------------"
	puts "#{Time.now} - Request"
	puts "---------------------------------------------------------------------------------"
  ap request
end

on_response do |response|
	puts "---------------------------------------------------------------------------------"
	puts "#{Time.now} - Response "
	puts "---------------------------------------------------------------------------------"
	ap response
	puts "#################################################################################"
end